CREATE OR REPLACE FUNCTION "public"."find_similar_articles"("query_embedding" "public"."vector", "match_threshold" double precision, "match_count" integer, "anchor_date" timestamp with time zone, "window_days" integer, "exclude_id" "uuid" DEFAULT NULL::"uuid") RETURNS TABLE("id" "uuid", "title" "text", "similarity" double precision, "cluster_id" "uuid", "published_at" timestamp with time zone, "source_name" "text")
    LANGUAGE "plpgsql"
    AS $$
BEGIN
    RETURN QUERY
    SELECT
        articles.id,
        articles.title,
        1 - (articles.embedding <=> query_embedding) AS similarity,
        articles.cluster_id,
        articles.published_at,
        articles.source_name
    FROM articles
    WHERE 
        -- Time Window: +/- window_days around the anchor date
        articles.published_at >= (anchor_date - (window_days || ' days')::interval)
        AND articles.published_at <= (anchor_date + (window_days || ' days')::interval)
        
        -- Vector Similarity
        AND 1 - (articles.embedding <=> query_embedding) > match_threshold
        
        -- Self Exclusion
        AND (exclude_id IS NULL OR articles.id != exclude_id)
    ORDER BY articles.embedding <=> query_embedding
    LIMIT match_count;
END;
$$;


