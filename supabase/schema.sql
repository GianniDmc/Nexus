CREATE OR REPLACE FUNCTION "public"."get_cluster_article_counts"() RETURNS TABLE("cluster_id" "uuid", "article_count" bigint)
    LANGUAGE "plpgsql"
    AS $$
BEGIN
  RETURN QUERY
  SELECT 
    a.cluster_id,
    COUNT(*)::bigint as article_count
  FROM articles a
  WHERE a.cluster_id IS NOT NULL
  GROUP BY a.cluster_id;
END;
$$;


